#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char *s2;
	unsigned char c2;

	s2 = s;
	c2 = c;
	while (n--)
	{
		*s2++ = c2;
	}
	return (s);
}
